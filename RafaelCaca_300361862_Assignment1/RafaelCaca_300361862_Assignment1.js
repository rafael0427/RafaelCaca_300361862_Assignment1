    var avatarContainer = document.getElementById('avatarContainer');
    var paginationContainer = document.getElementById('pagination');
    var itemsPerPage = 10;
    var currentPage = 1;

    function displayAvatars(page) {
        avatarContainer.innerHTML = '';
        var startIndex = (page - 1) * itemsPerPage;
        var endIndex = startIndex + itemsPerPage;

        for (var i = startIndex; i < endIndex && i < avatarArray.length; i++) {
            var avatar = avatarArray[i];
            var listItem = document.createElement('li');
            listItem.className = 'contact-item cf';

            var contactDetails = document.createElement('div');
            contactDetails.className = 'contact-details';

            var avatarImage = document.createElement('img');
            avatarImage.className = 'avatar';
            avatarImage.src = avatar.image;
            avatarImage.alt = avatar.name;

            var avatarName = document.createElement('h3');
            avatarName.textContent = avatar.name;

            var avatarEmail = document.createElement('span');
            avatarEmail.className = 'email';
            avatarEmail.textContent = avatar.email;

            contactDetails.appendChild(avatarImage);
            contactDetails.appendChild(avatarName);
            contactDetails.appendChild(avatarEmail);

            var joinedDetails = document.createElement('div');
            joinedDetails.className = 'joined-details';

            var avatarDate = document.createElement('span');
            avatarDate.className =  'joined';
            avatarDate.textContent ='Joined: ' + avatar.joined;

            joinedDetails.appendChild(avatarDate);

            listItem.appendChild(contactDetails);
            listItem.appendChild(joinedDetails);

            avatarContainer.appendChild(listItem);
        }
    }

    function displayPagination() {
        paginationContainer.innerHTML = '';
        var totalPages = Math.ceil(avatarArray.length / itemsPerPage);

        for (var i = 1; i <= totalPages; i++) {
            var pageButton = document.createElement('button');
            pageButton.textContent = i;
            pageButton.addEventListener('click', function () {
                currentPage = parseInt(this.textContent);
                displayAvatars(currentPage);
            });

            paginationContainer.appendChild(pageButton);
        }
    }

    displayAvatars(currentPage);
    displayPagination();

    
    function main(pageNum) {
        var nextPage = display * pageNum;
        var prevPage = display * (pageNum - 1);
        for (let i = 0; i < li.length; i++) {
            li[i].style.display = "none";
            if (i < nextPage && i >= prevPage) {
                li[i].style.display = "block";
            }
        }
    }

    function displayPagination() {
        paginationContainer.innerHTML = '';
        var totalPages = Math.ceil(avatarArray.length / itemsPerPage);

        var prevButton = document.createElement('button');
        prevButton.textContent = 'Prev';
        prevButton.addEventListener('click', function () {
            if (currentPage > 1) {
                currentPage--;
                displayAvatars(currentPage);
                highlightActiveButton(this);
            }
        });

        paginationContainer.appendChild(prevButton);

        for (var i = 1; i <= totalPages; i++) {
            var pageButton = document.createElement('button');
            pageButton.textContent = i;
            pageButton.addEventListener('click', function () {
                currentPage = parseInt(this.textContent);
                displayAvatars(currentPage);
                highlightActiveButton(this);
            });

            paginationContainer.appendChild(pageButton);
        }

        var nextButton = document.createElement('button');
        nextButton.textContent = 'Next';
        nextButton.addEventListener('click', function () {
            if (currentPage < totalPages) {
                currentPage++;
                displayAvatars(currentPage);
                highlightActiveButton(this);
            }
        });

        paginationContainer.appendChild(nextButton);

        highlightActiveButton(null);
    }

